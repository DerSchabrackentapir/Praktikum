// Addition

function sum (arr) {
  let a = arr[0];
  let b = arr[1];

  return a + b;
}
    
// Subtraktion  

function sub (arr) {
  let a = arr[0];
  let b = arr[1];

  return a - b;
}

// Multiplikation

function mult (arr) {
  let a = arr[0];
  let b = arr[1];

  return a * b;
}


// Division

function div (arr) {
  let a = arr[0];
  let b = arr[1];

  return a / b;
}


